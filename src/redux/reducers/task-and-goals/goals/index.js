const initState = {
    mymemberGoals: mymemberGoals,
    personalGoals: personalGoals
};

export const TaskAndGoalsGoalReducer = (state = initState, action) => {
    switch (action.type) {
        case "TASK_AND_GOALS_SEARCH_FOR_GOAL": {
            let afterSearch = state?.mymemberGoals?.filter((goals) => goals?.name.toLocaleLowerCase().startsWith(action?.searching.toLocaleLowerCase()))
            return { ...state, mymemberGoals: afterSearch };
        }
        default:
            return state;
    }
};



